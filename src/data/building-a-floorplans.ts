export type BuildingAParsedUnit = Pick<
  Unit,
  | "id"
  | "slug"
  | "externalCode"
  | "code"
  | "unitNumber"
  | "floor"
  | "typologyId"
  | "rooms"
  | "bedrooms"
  | "bathrooms"
  | "areaInternalSqm"
  | "areaTotalSqm"
  | "terraceSqm"
  | "hasYard"
  | "outdoorType"
  | "size"
  | "orientation"
  | "exposure"
  | "highlight"
  | "description"
  | "floorplan"
  | "floorplanImageId"
  | "features"
  | "planArea"
  | "planRegions"
  | "seoTitle"
  | "seoDescription"
>;

function createParsedUnit({
  floor,
  planArea,
  ...unit
}: Omit<BuildingAParsedUnit, "floorplan" | "floorplanImageId" | "planArea" | "floor"> & {
  floor: BuildingAFloorNumber;
  planArea: UnitPlanArea;
}): BuildingAParsedUnit {
  return {
    ...unit,
    floor,
    floorplan: buildingAFloorPlanImages[floor],
    floorplanImageId: buildingAFloorPlanMediaIds[floor],
    planArea,
  };
}

