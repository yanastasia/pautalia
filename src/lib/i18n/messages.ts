export function getMessages(locale: Locale) {
  return messages[locale];
}

export function getStatusLabel(locale: Locale, status: UnitStatus) {
  return messages[locale].status[status];
}

export function getResidenceLabel(locale: Locale, rooms: number) {
  return locale === "bg" ? `${rooms}-стаен апартамент` : `${rooms}-room apartment`;
}

export function getFloorLabel(locale: Locale, floor: number) {
  return locale === "bg" ? `Етаж ${floor}` : `Floor ${floor}`;
}

export function getBuildingLabel(locale: Locale, buildingId: string) {
  return locale === "bg" ? `Сграда ${buildingId.toUpperCase()}` : `Building ${buildingId.toUpperCase()}`;
}
